import Foundation

class Calculator{
    
    // 足し算
    func add(_ x:Int,_ y:Int) -> Int {
        return x + y
    }
    
    // 引き算
    func subtract(_ x:Int,_ y:Int) -> Int {
        return x - y
    }
    
    // 掛け算
    func multiple(_ x:Int,_ y:Int) -> Int {
        return x * y
    }
    
    // 割り算
    func divide(_ x:Int,_ y:Int) -> Int {
        return x / y
    }
}
